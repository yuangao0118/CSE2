public class TextSpinner {
    public static void main(String[] args) {
        
        while (true) {
            System.out.print("/");
            System.out.print("\b");
            System.out.print("-");
            System.out.print("\b");
            System.out.print("\\");
            System.out.print("\b");
            System.out.print("|");
            System.out.print("\b");
        }
    }
}